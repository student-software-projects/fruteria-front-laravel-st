    De fragante pulpa, el mangostino es un fruto dulce y agrio a la vez, con sabor cítrico y textura de melocotón,
    tan delicioso que cuenta la leyenda que la Reina Victoria llegó a ofrecer una recompensa a quien le trajera esta fruta.<br><br>
