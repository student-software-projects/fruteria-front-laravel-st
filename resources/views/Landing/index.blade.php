4. Zapote:<br>
    Y esto tan negro y desagradable a la vista es en realidad una sabrosa fruta de usos medicinales.
    El zapote negro es originario de mesoamérica y tiene una textura y un aroma que recuerda al flan de chocolate.<br><br>
    <img height="300" width="300" src="https://www.llaollaoweb.com/blog/wp-content/uploads/2016/02/Zapote-negro-e1456313872492.jpg"><br><br>
5. Durio:<br>
    Seguro que ninguno de nosotros se acercaría a menos de un metro de un durio,
